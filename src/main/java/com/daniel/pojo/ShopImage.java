package com.daniel.pojo;

public class ShopImage {

    private int id;
    private Shop shop;
    private int type;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Shop getShop() {
        return shop;
    }

    public void setShop(Shop shop) { this.shop = shop; }

    public int getType() { return type; }

    public void setType(int type) { this.type = type; }
}
